package day1;

public class Java1_주석 {
	public static void main(String[] args) {
		// 주석은 메모, 문서의 역할, 설명, 주석은 컴파일되지 않음.
		// 문자는 큰따옴표("")안에 작성
		System.out.println(1);
		System.out.println(2);
		System.out.println("안녕하세요");
		
		// 주석은 슬래시 두개(//) 직접 입력 or Ctrl+/ 해제하고싶으면 다시
	} // main 
} // class 
